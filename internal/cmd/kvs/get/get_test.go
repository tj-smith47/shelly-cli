func TestOptions(t *testing.T) {
	t.Parallel()

	opts := &Options{
		Device: "test-device",
		Key:    "test-key",
		Raw:    true,
	}

	if opts.Device != "test-device" {
		t.Errorf("Device = %q, want %q", opts.Device, "test-device")
	}
	if opts.Key != "test-key" {
		t.Errorf("Key = %q, want %q", opts.Key, "test-key")
	}
	if !opts.Raw {
		t.Error("Raw should be true")
	}
}

