func TestOptions(t *testing.T) {
	t.Parallel()

	f := cmdutil.NewFactory()
	opts := &Options{
		Factory: f,
		Device:  "test-device",
		Method:  "Shelly.GetStatus",
		Params:  `{"id":0}`,
		Raw:     true,
	}

	if opts.Device != "test-device" {
		t.Errorf("Device = %q, want %q", opts.Device, "test-device")
	}

	if opts.Method != "Shelly.GetStatus" {
		t.Errorf("Method = %q, want %q", opts.Method, "Shelly.GetStatus")
	}

	if opts.Params != `{"id":0}` {
		t.Errorf("Params = %q, want %q", opts.Params, `{"id":0}`)
	}

	if !opts.Raw {
		t.Error("Raw should be true")
	}
}

